package org.mach.springdao;

public class ReportIncidence {
	String reportedMessage;
	String noPeople;
	String helpNeeded;
	public String getReportedMessage() {
		return reportedMessage;
	}
	public void setReportedMessage(String reportedMessage) {
		this.reportedMessage = reportedMessage;
	}
	public String getNoPeople() {
		return noPeople;
	}
	public void setNoPeople(String noPeople) {
		this.noPeople = noPeople;
	}
	public String getHelpNeeded() {
		return helpNeeded;
	}
	public void setHelpNeeded(String helpNeeded) {
		this.helpNeeded = helpNeeded;
	}
	
	
}
